// addition function
function addition(num1, num2) {
     return num1 + num2;
};
// subtraction function
function subtraction(num1, num2) {
     return num1 - num2;
};
// multiplication function
function multiplication(num1, num2) {
     return num1 * num2;
};
// division function
function division(num1, num2) {
     return num1 / num2;
};